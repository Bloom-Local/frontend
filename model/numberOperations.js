export function isGreaterOrEqualThan(num1, num2) {
    return num1 >= num2;
}

export function isSmallerOrEqualThan(num1, num2) {
    return num1 <= num2;
}

export function isSmallerThan(num1, num2) {
    return num1 < num2;
}

export function isGreaterThan(num1, num2) {
    return num1 > num2;
}
